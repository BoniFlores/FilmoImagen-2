    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        ConPeliculas c= new ConPeliculas();
        DTOPelicula peli= new DTOPelicula();
        peli.setNombre(jTextField1.getText());
        peli.setGenero(jTextField2.getText());
        peli.setPrecio(Double.parseDouble(jTextField3.getText()));
        if (c.insertar(peli)) {
            JOptionPane.showMessageDialog(null, "Se Agrego Correctamente");
            limpiarTabla();
            llenartabla();
        } else {
            JOptionPane.showMessageDialog(null, "No se Agrego Correctamente");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

