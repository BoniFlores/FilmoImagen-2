    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        int resp = 0;
        DTOPelicula cat = new DTOPelicula();
        cat.setNombre(jTable1.getValueAt(fila, 1).toString());
        ConPeliculas conc = new ConPeliculas();
        resp = JOptionPane.showConfirmDialog(this, "Esta seguro", "Alerta!", JOptionPane.YES_NO_OPTION);
        if (resp == 0) {
            if (new ConPeliculas().eliminar(cat)) {
                JOptionPane.showMessageDialog(this, "Se elimino correctamente");
                limpiarTabla();
                llenartabla();
            } else {
                JOptionPane.showMessageDialog(this, "No se elimino correctamente");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

