    private void llenartabla() {
        ArrayList<DTOPelicula> listapro = new DAO().consultarTodo();
        for (DTOPelicula ca : listapro) {
            MODELOTABLA.addRow(new String[]{
                String.valueOf(ca.getId()),
                ca.getNombre(),
                String.valueOf(ca.getGenero()),
                        String.valueOf(ca.getPrecio())
            });
        }
    }
    
    public void limpiarTabla() {
        int fila = jTable1.getRowCount();
        for (int i = 0; i < fila; i++) {
            MODELOTABLA.removeRow(0);
        }
    }

